#include "libft.h"

int	ft_atoi(const char *nptr)
{
	int	sign;
	int	convert_value;

	sign = 1;
	convert_value = 0;
	while ((*nptr >= '\t' && *nptr <= '\r') || *nptr == ' ')
		nptr++;
	if (*nptr == '-' || *nptr == '+')
	{
		if (*nptr == '-')
			sign = -1;
		nptr++;
	}
	while (*nptr >= '0' && *nptr <= '9')
	{
		convert_value = (convert_value * 10) + (*nptr - '0');
		nptr++;
	}
	return (convert_value * sign);
}
